CREATE TABLE WORKER (
WORKER_ID INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
FIRST_NAME CHAR(25),
LAST_NAME CHAR(25),
SALARY INT(25),
JOINING_DATE datetime,
DEPARTMENT CHAR(25)
);

